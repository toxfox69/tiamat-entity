async function getFinancialState(
  conway: ConwayClient,
  address: string,
): Promise<FinancialState> {
  let creditsCents = 0;
  let usdcBalance = 0;

  try {
    creditsCents = await conway.getCreditsBalance();
  } catch {}

  try {
    usdcBalance = await getUsdcBalance(address as `0x${string}`);
  } catch {}

  // TIAMAT uses Anthropic API key directly, NOT Conway credits.
  // Conway credits will always be 0. getUsdcBalance returns 0 on RPC failure
  // (it catches internally, never throws). Always set a survival floor
  // so TIAMAT never enters "dead" from a stale Conway/RPC check.
  if (creditsCents === 0) {
    creditsCents = 500; // $5 virtual floor — TIAMAT's survival is not tied to Conway
  }

  return {
    creditsCents,
    usdcBalance,
    lastChecked: new Date().toISOString(),
  };
}

function estimateCostCents(
  usage: { promptTokens: number; completionTokens: number },
  model: string,
): number {
  // Rough cost estimation per million tokens
  // Uses prefix matching so "claude-haiku-4-5-20251001" matches "claude-haiku"
  const pricing: Array<{ match: string; input: number; output: number }> = [
    { match: "claude-sonnet",  input: 300,  output: 1500 },
    { match: "claude-haiku",   input: 100,  output: 500  },
    { match: "claude-opus",    input: 1500, output: 7500 },
    { match: "gpt-4o-mini",    input: 15,   output: 60   },
    { match: "gpt-4o",         input: 250,  output: 1000 },
    { match: "gpt-4.1-nano",   input: 10,   output: 40   },
    { match: "gpt-4.1-mini",   input: 40,   output: 160  },
    { match: "gpt-4.1",        input: 200,  output: 800  },
    { match: "o3-mini",        input: 110,  output: 440  },
    { match: "o4-mini",        input: 110,  output: 440  },
    { match: "o1",             input: 1500, output: 6000 },
    { match: "llama",          input: 0,    output: 0    },  // Free tier (Groq)
    { match: "gpt-oss",        input: 0,    output: 0    },  // Free tier (Cerebras)
    { match: "gemini",         input: 0,    output: 0    },  // Free tier
    { match: "gemma",          input: 0,    output: 0    },  // Free tier (OpenRouter)
    { match: "mistral",        input: 0,    output: 0    },  // Free tier (OpenRouter)
    { match: "claude-code",    input: 0,    output: 0    },  // CLI subscription (free)
  ];

  const p = pricing.find(p => model.includes(p.match)) || { input: 100, output: 500 };
  const inputCost = (usage.promptTokens / 1_000_000) * p.input;
  const outputCost = (usage.completionTokens / 1_000_000) * p.output;
  return Math.ceil((inputCost + outputCost) * 1.3); // 1.3x Conway markup
}

function stuckKey(toolName: string, args: Record<string, unknown>, error: string): string {
  const argsSnippet = JSON.stringify(args).slice(0, 150);
  const errSnippet = error.slice(0, 100);
  return `${toolName}::SEP::${argsSnippet}::SEP::${errSnippet}`;
}

/**
 * Filter training data hallucinations from OSS model output.
 * Models like 120B/70B/nemo sometimes regurgitate math problems,
 * random import records, or benchmark data instead of useful thoughts.
 * Also strips Qwen3 <think>...</think> tags — extracts only the actionable output.
 */
function filterTrainingJunk(text: string): string {
  if (!text || text.length < 5) return text;

  // Strip <think>...</think> blocks (Qwen3 reasoning traces)
  // Keep only the content AFTER the closing </think> tag
  let cleaned = text;
  if (cleaned.includes('<think>')) {
    const closeIdx = cleaned.lastIndexOf('</think>');
    if (closeIdx !== -1) {
      // Content after </think> is the actual response
      const afterThink = cleaned.slice(closeIdx + 8).trim();
      // If there's meaningful content after think, use it; otherwise use the think content
      if (afterThink.length > 5) {
        cleaned = afterThink;
      } else {
        // Extract just the think content without tags (model only produced reasoning)
        cleaned = cleaned.replace(/<\/?think>/g, '').trim();
      }
    } else {
      // Opening <think> but no closing — strip the tag and keep content
      cleaned = cleaned.replace(/<think>/g, '').trim();
    }
  }

  const lines = cleaned.split('\n');
  const filtered = lines.filter(line => {
    const l = line.trim();
    if (!l) return true; // keep blank lines
    // Math problem hallucinations: "Q: Let x = ...", "Q: Suppose ...", "Q: Solve ..."
    if (/^Q:\s*(Let|Suppose|Solve|Expand|What|Find|Calculate|Simplify|Divide|Round)/i.test(l)) return false;
    // Answer lines from math: "A: ...", "Answer: ..."
    if (/^(A:|Answer:)\s*[-\d]/i.test(l)) return false;
    // German import/export records
    if (/^[A-Z]{3,}(REIMPORT|EXPORT|IMPORT)\s+\d{4}/i.test(l)) return false;
    // Random data table rows (all caps + numbers)
    if (/^[A-Z\s]{10,}\d{4}\s+\d{2}$/.test(l)) return false;
    // Repeated math expressions with no context
    if (/^\d+\s*[\+\-\*\/]\s*\d+\s*[\+\-\*\/=]/.test(l) && l.length < 30) return false;
    // "The expression simplifies to..." without context
    if (/^The expression simplifies to/i.test(l)) return false;
    // "Question: What is the nearest..." benchmark patterns
    if (/^Question:\s*(What|Which|How|Is|Does|Solve)/i.test(l)) return false;
    return true;
  });
  return filtered.join('\n').trim();
}

function log(config: AutomatonConfig, message: string): void {
  if (config.logLevel === "debug" || config.logLevel === "info") {
    const timestamp = new Date().toISOString();
    console.log(`[${timestamp}] ${message}`);
  }
}

