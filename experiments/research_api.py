def _generate_report(topic: str) -> tuple:
    """Run Groq inference. Returns (report_text, word_count, ms_elapsed)."""
    t0 = time.time()
    completion = groq_client.chat.completions.create(
        model="llama-3.3-70b-versatile",
        messages=[
            {"role": "system", "content": _RESEARCH_SYSTEM},
            {"role": "user", "content": f"Generate a comprehensive research report on: {topic}"}
        ],
        temperature=0.35,
        max_tokens=3500,
    )
    text = completion.choices[0].message.content
    elapsed_ms = int((time.time() - t0) * 1000)
    word_count = len(text.split())
    return text, word_count, elapsed_ms


