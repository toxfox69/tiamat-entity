# ── POST /research — Core API ─────────────────────────────────
@app.route("/research", methods=["POST"])
def research_api():
    ip = _get_ip()
    try:
        # ── Rate limit (abuse prevention) ────────────────────
        rl = _rate_limiter.check(ip, scope="research")
        if not rl.allowed:
            _log(ip, False, 429, "rate-limited")
            return jsonify({
                "error": "Too many requests. Try again later.",
                "retry_after_seconds": int(rl.retry_after_sec)
            }), 429
        _rate_limiter.record(ip, scope="research")

        # ── Parse input ──────────────────────────────────────
        data = request.get_json(force=True, silent=True) or {}
        topic = str(data.get("topic", "")).strip()
        if not topic:
            return jsonify({"error": 'Missing "topic" field'}), 400
        if len(topic) > MAX_TOPIC_LEN:
            return jsonify({"error": f"Topic too long (max {MAX_TOPIC_LEN} chars)"}), 400

        # ── Payment check (real on-chain, $1.00 USDC) ────────
        tx_hash = extract_payment_proof(request)
        paid = False
        if tx_hash:
            vr = verify_payment(tx_hash, REPORT_PRICE_USDC, endpoint="/research")
            if not vr["valid"]:
                _log(ip, False, 402, f"payment rejected: {vr['reason']}")
                resp = payment_required_response(REPORT_PRICE_USDC, endpoint="/research")
                resp["payment_error"] = vr["reason"]
                return jsonify(resp), 402
            paid = True

        # ── Free quota check ─────────────────────────────────
        if not paid:
            has_quota, remaining = _check_free_quota(ip)
            if not has_quota:
                _log(ip, False, 402, "daily quota exceeded")
                resp = payment_required_response(REPORT_PRICE_USDC, endpoint="/research")
                resp["message"] = "You've used your 1 free research report for today."
                return jsonify(resp), 402
        else:
            remaining = "N/A (paid)"

        # ── Generate report ──────────────────────────────────
        try:
            report_text, word_count, gen_ms = _generate_report(topic)
        except Exception as gen_err:
            err_str = str(gen_err)
            _log(ip, paid, 503, f"inference error: {err_str[:120]}")
            if "rate_limit" in err_str or "429" in err_str:
                return jsonify({
                    "error": "inference_busy",
                    "message": "Inference service rate limit reached. Please retry in a few minutes.",
                }), 503
            return jsonify({"error": "Report generation failed. Please retry."}), 503

        _log(ip, paid, 200, f"topic={topic[:60]!r} words={word_count} ms={gen_ms}")

        return jsonify({
            "report": report_text,
            "topic": topic,
            "word_count": word_count,
            "generation_ms": gen_ms,
            "charged": paid,
            "free_calls_remaining": remaining,
            "model": "groq/llama-3.3-70b",
        }), 200

    except Exception as e:
        _log(ip, False, 500, str(e))
        return jsonify({"error": "Internal server error"}), 500


